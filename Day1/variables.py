# Basic numeric variables and arithmetic
a = 5
b = 3

total = a + b        # addition
difference = a - b   # subtraction
product = a * b      # multiplication
division = a / b     # division (float)
remainder = a % b    # modulus
power = a ** b       # exponentiation

print("Numeric operations (a = 5, b = 3):")
print("  Sum:", total)
print("  Difference:", difference)
print("  Product:", product)
print("  Division:", division)
print("  Remainder:", remainder)
print("  Power (a ** b):", power)
print()


# Example: storing personal information in variables
first_name = "Muhammad"
last_name = "Fayaz"
country = 'Romania'
city = "Bucharest"

current_year = 2026
birth_year = 1998
age = current_year - birth_year

print("Personal information:")
print(f"  Name: {first_name} {last_name}")
print(f"  Location: {city}, {country}")
print(f"  Birth year: {birth_year} (Age: {age})")

